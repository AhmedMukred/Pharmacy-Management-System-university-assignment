    private boolean assignMedicineDetails() throws IOException, ParseException {
        boolean success = false;
        Pharmacist phar = new Pharmacist();
        ArrayList<String> details = new ArrayList();
        TxtFile mdc = new TxtFile("medicine.txt");
        details = mdc.getMedicineDetailsFromFile(oldMedicineId_txt.getText(),
                oldmedicineName_txt.getText());
        if (details.get(0).equals("true")) {
            Calendar domCal = Calendar.getInstance();
            Calendar doeCal = Calendar.getInstance();
            SimpleDateFormat sdf = new SimpleDateFormat("MMM dd, yyyy", Locale.ENGLISH);
            domCal.setTime(sdf.parse(details.get(5)));
            doeCal.setTime(sdf.parse(details.get(6)));
            success = true;
            newMedicineId_txt.setText(details.get(1));
            newMedicineName_txt.setText(details.get(2));
            companyName_txt.setText(details.get(3));
            batch_txt.setText(details.get(4));
            DOM_dateChooser.setSelectedDate(domCal);
            DOE_dateChooser.setSelectedDate(doeCal);
            price_txt.setText(details.get(7));
            quantity_txt.setText(details.get(8));
        }
        return success;
    }

    private boolean checkFields() {
        boolean success = false;
        if (newMedicineId_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered the medecine ID",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
            newMedicineId_txt.requestFocusInWindow();
        } else if (newMedicineName_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered the medicine name",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
            newMedicineName_txt.requestFocusInWindow();
        } else if (companyName_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered the company name",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
            companyName_txt.requestFocusInWindow();
        } else if (batch_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered the batch",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
            batch_txt.requestFocusInWindow();
        } else {
            
            try {
                Double.parseDouble(price_txt.getText());
                success = true;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                        + "have entered a correct price. Text filed only accepts numbers.",
                        "Invalid Entry!",
                        JOptionPane.ERROR_MESSAGE);
                price_txt.requestFocusInWindow();
                return false;
            }
            
            try {
                Integer.parseInt(batch_txt.getText());
                success = true;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                        + "have entered correct batch number. Text filed only accepts numbers. ",
                        "Invalid Entry!",
                        JOptionPane.ERROR_MESSAGE);
                batch_txt.requestFocusInWindow();
                return false;
            }

            try {
                Integer.parseInt(quantity_txt.getText());
                success = true;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                        + "have entered correct quantity number. Text filed only accepts numbers. ",
                        "Invalid Entry!",
                        JOptionPane.ERROR_MESSAGE);
                quantity_txt.requestFocusInWindow();
                return false;
            }

        }
        return success;
    }

    private void clearFields() {
        newMedicineId_txt.setText(null);
        newMedicineName_txt.setText(null);
        companyName_txt.setText(null);
        batch_txt.setText(null);
        price_txt.setText(null);
        quantity_txt.setText(null);
        DOE_dateChooser.setSelectedDate(Calendar.getInstance());
        DOM_dateChooser.setSelectedDate(Calendar.getInstance());
        newMedicineId_txt.requestFocusInWindow();
    }

