    private void modify_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modify_btnActionPerformed
        if (checkFields()) {
            Pharmacist phr = new Pharmacist();
            try {
                if (phr.modifyMedicine(oldMedicineId_txt.getText(),
                        oldmedicineName_txt.getText(), newMedicineId_txt.getText(),
                        newMedicineName_txt.getText(), companyName_txt.getText(),
                        Integer.parseInt(batch_txt.getText()), DOM_dateChooser.getText(),
                        DOE_dateChooser.getText(), Double.parseDouble(price_txt.getText()),
                        Integer.parseInt(quantity_txt.getText())) == true) {
                    JOptionPane.showMessageDialog(frame, newMedicineId_txt.getText()
                            + " has been modifies successfully",
                            "Successful Process", JOptionPane.INFORMATION_MESSAGE);
                    medicineDetails_Pnl.setVisible(false);
                    oldMedicineId_txt.setEnabled(true);
                    oldmedicineName_txt.setEnabled(true);
                    OK_btn.setEnabled(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(frame, newMedicineId_txt.getText()
                            + " has not been modifies successfully",
                            "Faild Process", JOptionPane.ERROR_MESSAGE);
                }
            } catch (IOException ex) {
                Logger.getLogger(ModifyMedicine.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_modify_btnActionPerformed

