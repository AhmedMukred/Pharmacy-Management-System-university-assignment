    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        if (checkFields() == true) {
            Admin dmn = new Admin();
            try {
                if (dmn.addNewUser(firstName_txt.getText(), lastName_txt.getText(),
                        email_txt.getText(), newUserNameTxt.getText(),
                        String.valueOf(pwdTxt.getPassword()),
                        String.valueOf(roleCmb.getSelectedItem())) == true) {
                    JOptionPane.showMessageDialog(frame,
                            newUserNameTxt.getText() + " has added successfully as a new username",
                            "Successful Adding New User", JOptionPane.INFORMATION_MESSAGE);
                    clearFields();
                    addUser_pnl.setVisible(false);
                    this.dispose();
                }
            } catch (IOException ex) {
                Logger.getLogger(UsersManagement.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_add_btnActionPerformed

