    private boolean checkFields() {
        boolean success = false;
        if (firstName_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered user's first name",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
        } else if (lastName_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered user's last name",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!email_txt.getText().matches("[a-zA-Z0-9\\.]+@[a-zA-Z0-9\\-\\_\\.]+\\.[a-zA-Z0-9]{3}")) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered a correct email address",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
        } else if (newUserNameTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered the new username",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
        } else if (String.valueOf(pwdTxt.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have entered the password",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
        } else if (String.valueOf(rePwdTxt.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(frame, "Please, make sure that you "
                    + "have re-written the password",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
        } else if (String.valueOf(pwdTxt.getPassword()) == null
                ? String.valueOf(rePwdTxt.getPassword()) != null
                : !String.valueOf(pwdTxt.getPassword()).equals(String.valueOf(rePwdTxt.getPassword()))) {
            JOptionPane.showMessageDialog(frame, "Please, Re-Write the password."
                    + "Password fields are not match",
                    "Invalid Entry!",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            success = true;
        }
        return success;
    }

    private void getUsersList() throws IOException {
        users_cmb.removeAllItems();
        TxtFile file = new TxtFile("users");
        ArrayList<String> list = new ArrayList<>();
        list = file.getUsersListFromFile();
        for (int i = 0; i < list.size(); i += 3) {
            users_cmb.addItem(list.get(i).substring(9));
        }
    }

