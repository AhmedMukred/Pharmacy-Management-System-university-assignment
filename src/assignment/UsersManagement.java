    private void OK_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OK_btnActionPerformed
        if (option_cmb.getSelectedItem() == "Add New User") {
            addUser_pnl.setVisible(true);
            removeUser_pnl.setVisible(false);
            options_pnl.setVisible(false);
        } else {
            try {
                getUsersList();
                options_pnl.setVisible(false);
            } catch (IOException ex) {
                ex.getStackTrace();
            }
            addUser_pnl.setVisible(false);
            removeUser_pnl.setVisible(true);
        }
    }//GEN-LAST:event_OK_btnActionPerformed

